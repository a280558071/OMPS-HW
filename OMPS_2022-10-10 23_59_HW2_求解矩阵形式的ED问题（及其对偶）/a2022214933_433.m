function[x,u,p,w]=Gurobi_ED
clc;
clear;
yalmip('clear');
x=sdpvar(2,1);
u=sdpvar(4,1);
C=[40,50];
A=[1,2;4,3;1,1;-1,-1];
B=[40;120;30;-30];
cons1=[A*x<=B,x(1)>=0,x(2)>=0];
p=-(C*x);
cons2=[A.'*u>=C.',u(1)>=0,u(2)>=0,u(3)>=0,u(4)>=0];
w=B.'*u;
ops=sdpsettings('solver','gurobi','showprogress',1);
optimize(cons1,p,ops);
optimize(cons2,w,ops);
value(x)
-value(p)
value(u)
value(w)
end