clc;
clear;
g1=sdpvar(2,1);
z=g1(1)^2+40*g1(1)+g1(2)^2+50*g1(2);
I=[1;1;2;3;4];
J=[2;3;3;4;2];
G=digraph(I,J);
P1=plot(G);
IN=incidence(G);
g=[g1(1),g1(2),0,0]';
d=[0,0,10,20]';
X=[0.01,0.01,0.01,0.02,0.01]';
p=sdpvar(5,1);
theta=sdpvar(4,1);
Cons=[g1(1)+2*g1(2)<=40,4*g1(1)+3*g1(2)<=120,g1(1)>=0,g1(2)>=0,IN*p==g-d,IN'*theta==p.*X,theta(1)==0,p<=20];
ops=sdpsettings('solver','gurobi');
sol=optimize(Cons,z,ops);
s_g=value(g),s_z=value(z)
s_p=value(p)
s_theta=value(theta)
plot(Cons);
axis([0,60,0,50]);
